import { readFile, writeFile } from "node:fs/promises";
import { checklistPath, matrixDocsDir, openApiRowMapPath, openApiUnitsPath } from "./paths.mjs";
import { buildChecklistIndex, extractChecklistRows } from "./checklist-parser.mjs";

const endpointDocPath = `${matrixDocsDir}/speccheck-matrix-v2-endpoint.md`;

function uniquePreserveOrder(values) {
  const seen = new Set();
  const out = [];
  for (const v of values) {
    if (!v || seen.has(v)) continue;
    seen.add(v);
    out.push(v);
  }
  return out;
}

function escapeMdCell(value) {
  // Markdown table cells: avoid literal pipes breaking the table.
  return String(value ?? "").replaceAll(/\|/g, "\\|");
}

async function main() {
  const checklist = await readFile(checklistPath, "utf8");
  const checklistRows = extractChecklistRows(checklist);
  const checklistIndex = buildChecklistIndex(checklistRows);

  const rowMetaById = new Map();
  for (const [rowId, row] of checklistIndex.byRowId.entries()) {
    rowMetaById.set(rowId, { title: row.title, section: row.section });
  }

  const units = JSON.parse(await readFile(openApiUnitsPath, "utf8"));
  const openapiRowMap = JSON.parse(await readFile(openApiRowMapPath, "utf8"));

  const operationUnits = units.units.filter((unit) => unit.kind === "operation");

  // rowId -> list of mapped operations (operation appears in every mapped row).
  const operationsByRowId = new Map();

  for (const operation of operationUnits) {
    const fileKey = `${operation.spec}/${operation.file}`;
    const fileMapping = openapiRowMap[fileKey];
    if (!fileMapping?.operations) continue;

    const operationMapping =
      fileMapping.operations[operation.operationId] ||
      fileMapping.operations[operation.operationKey];
    if (!operationMapping) continue;

    const rowIds = [
      ...(operationMapping.rowIds || []),
      ...(operationMapping.rowId ? [operationMapping.rowId] : []),
    ].filter(Boolean);

    // Only include operations mapped to known primary checklist rows.
    const uniqueRowIds = [...new Set(rowIds)].filter((rowId) => rowMetaById.has(rowId));
    if (uniqueRowIds.length === 0) continue;

    const unitIds = [
      ...new Set([
        ...(operationMapping.unitIds || []),
        ...(operationMapping.unitId ? [operationMapping.unitId] : []),
      ]),
    ];

    for (const rowId of uniqueRowIds) {
      const list = operationsByRowId.get(rowId) || [];
      list.push({
        operationId: operation.operationId,
        method: operation.method.toUpperCase(),
        path: operation.path,
        operationKey: operation.operationKey,
        unitIds,
      });
      operationsByRowId.set(rowId, list);
    }
  }

  // Deterministic rendering: row order follows checklist order.
  const orderedRowIds = uniquePreserveOrder(checklistRows.map((r) => r.rowId));

  for (const [rowId, list] of operationsByRowId.entries()) {
    list.sort((a, b) => {
      const opA = `${a.method} ${a.path} ${a.operationId}`;
      const opB = `${b.method} ${b.path} ${b.operationId}`;
      return opA.localeCompare(opB);
    });

    // De-dup identical operation rows within the same rowId group.
    const seen = new Set();
    operationsByRowId.set(
      rowId,
      list.filter((op) => {
        const key = `${op.method} ${op.path} ${op.operationId} ${op.operationKey}`;
        if (seen.has(key)) return false;
        seen.add(key);
        return true;
      }),
    );
  }

  let md = `# speccheck-matrix-v2 Endpoint Map\n\n`;
  md += `This document is generated from \`docs/spec-coverage/openapi-row-map.json\` (operation mapping) plus \`docs/spec-coverage/openapi-units.json\` (operation inventory).\n\n`;
  md += `Row groups are keyed by \`row_id\` from \`docs/matrix/speccheck-matrix-v2.md\`.\n`;
  md += `It is a “導線” view: this file does not participate in coverage target extraction.\n\n`;

  for (const rowId of orderedRowIds) {
    const meta = rowMetaById.get(rowId);
    if (!meta) continue;
    const ops = operationsByRowId.get(rowId) || [];

    md += `## ${meta.title} (${rowId})\n\n`;
    md += `Mapped operations: ${ops.length}\n\n`;

    if (ops.length === 0) {
      md += `_No operations mapped yet._\n\n`;
      continue;
    }

    md += `| operationId | method | path | operationKey |\n`;
    md += `|-------------|--------|------|--------------|\n`;
    for (const op of ops) {
      md += `| \`${escapeMdCell(op.operationId)}\` | \`${escapeMdCell(op.method)}\` | ${escapeMdCell(op.path)} | \`${escapeMdCell(op.operationKey)}\` |\n`;
    }

    md += `\n`;
  }

  md += `<!-- Generated by scripts/spec/generate-speccheck-matrix-v2-endpoint-doc.mjs -->\n`;

  await writeFile(endpointDocPath, `${md}`, "utf8");
  console.log(`Generated ${endpointDocPath}`);
}

await main();
